"""## 2. التغليف (Encapsulation)

التغليف هو مبدأ برمجي يجمع البيانات (الخصائص) والأساليب (الوظائف) التي تعمل على تلك البيانات في وحدة واحدة (الفئة)، مع إخفاء التفاصيل الداخلية عن العالم الخارجي والتحكم في الوصول إلى البيانات. الهدف الرئيسي هو حماية البيانات من التعديل غير المقصود وضمان تناسق الكائن.

### 2.1 الخصائص الخاصة (Private Properties)

في بايثون، لا يوجد مفهوم حقيقي للخصائص "الخاصة" بالمعنى الصارم كما في بعض اللغات الأخرى. ومع ذلك، يوجد اصطلاح قوي لتمثيل الخصائص الخاصة باستخدام **بادئة الشرطة السفلية المزدوجة (`__`)**.

عند استخدام `__`، تقوم بايثون بتشويه الاسم (Name Mangling) لجعل الوصول إليه من خارج الفئة أكثر صعوبة (ولكن ليس مستحيلاً).
"""

# مثال على خاصية "خاصة" باستخدام __age

class Person:
  def __init__(self, name, age):
    self.name = name
    self.__age = age # خاصية يفترض أنها خاصة

p1 = Person("أحمد", 30)
print(f"اسم الشخص: {p1.name}")

# محاولة الوصول مباشرة إلى الخاصية الخاصة ستؤدي إلى خطأ
try:
  print(f"عمر الشخص: {p1.__age}")
except AttributeError as e:
  print(f"حدث خطأ عند محاولة الوصول لـ __age مباشرة: {e}")

"""### 2.2 الوصول إلى الخصائص الخاصة: دوال الجلب (Getters)

للوصول إلى قيمة خاصية خاصة بطريقة منظمة وآمنة، نستخدم دوال الجلب (Getter methods). هذه الدوال تُعرّف داخل الفئة وتُعيد قيمة الخاصية.
"""

# مثال على استخدام دالة جلب (Getter) للوصول إلى خاصية خاصة

class Person:
  def __init__(self, name, age):
    self.name = name
    self.__age = age

  def get_age(self):
    return self.__age

p1 = Person("ليلى", 25)
