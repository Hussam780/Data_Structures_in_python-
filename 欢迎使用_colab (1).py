"""# 4. التعامل مع الاستثناءات (Exception Handling)

الاستثناءات هي الأخطاء التي تحدث أثناء تنفيذ البرنامج وتتسبب في إيقافه بشكل غير طبيعي. يسمح لك التعامل مع الاستثناءات بجعل برامجك أكثر قوة ومرونة، حيث يمكنك اعتراض الأخطاء والتعامل معها بأمان بدلاً من السماح للبرنامج بالتعطل.

### 4.1 جمل `try`, `except`, `else`, `finally`

تُستخدم هذه الجمل للتعامل مع الاستثناءات:

*   **`try`**: الكتلة التي تحتوي على الكود الذي قد يرفع استثناءً.
*   **`except`**: الكتلة التي يتم تنفيذها إذا حدث استثناء في كتلة `try`. يمكنك تحديد نوع الاستثناء الذي تريد اعتراضه.
*   **`else`**: (اختياري) الكتلة التي يتم تنفيذها إذا لم يتم رفع أي استثناء في كتلة `try`.
*   **`finally`**: (اختياري) الكتلة التي يتم تنفيذها دائمًا، سواء حدث استثناء أم لا. تُستخدم عادةً لتنظيف الموارد (مثل إغلاق الملفات).
"""

# مثال على استخدام try-except

def divide(a, b):
  try:
    result = a / b
  except ZeroDivisionError:
    print("خطأ: لا يمكن القسمة على صفر!")
  except TypeError:
    print("خطأ: يجب أن تكون المدخلات أرقامًا.")
  else:
    print(f"النتيجة هي: {result}")
  finally:
    print("انتهت محاولة القسمة.")

print("\n-- الحالة الأولى: قسمة ناجحة --")
divide(10, 2)

print("\n-- الحالة الثانية: قسمة على صفر --")
divide(10, 0)

print("\n-- الحالة الثالثة: خطأ في النوع --")
divide(10, "خمسة")

print("\n-- الحالة الرابعة: بدون أخطاء، مع else --")
try:
  num = int("123")
except ValueError:
  print("ليس رقمًا صحيحًا.")
else:
  print(f"تم تحويل الرقم بنجاح: {num}")
finally:
  print("تم الانتهاء من عملية التحويل.")

"""### 4.2 رفع الاستثناءات (Raising Exceptions)

يمكنك أيضًا رفع استثناءات مخصصة باستخدام جملة `raise` عندما تكتشف حالة لا يجب أن تحدث في برنامجك.
"""

# مثال على رفع الاستثناءات

def set_age(age):
  if not isinstance(age, int) or age <= 0:
    raise ValueError("العمر يجب أن يكون عددًا صحيحًا موجبًا.")
  print(f"تم تعيين العمر إلى {age}")

try:
  set_age(30)
  set_age(-5) # هذا سيرفع استثناءً
except ValueError as e:
  print(f"حدث خطأ في تعيين العمر: {e}")

try:
  set_age("عشرون")
except ValueError as e:
  print(f"حدث خطأ آخر في تعيين العمر: {e}")

"""### 4.3 الاستخدام العملي: دمج التعامل مع الملفات والاستثناءات

غالبًا ما يتم استخدام التعامل مع الاستثناءات عند العمل مع الملفات لحماية البرنامج من مشكلات مثل عدم وجود الملفات، أو عدم وجود أذونات الوصول، أو تلف البيانات.
"""

# مثال على دمج قراءة الملفات والتعامل مع الاستثناءات

def read_file_safely(filename):
  try:
    with open(filename, 'r', encoding='utf-8') as f:
      content = f.read()
      print(f"\nمحتوى الملف '{filename}':")
      print(content)
  except FileNotFoundError:
    print(f"خطأ: الملف '{filename}' غير موجود.")
  except PermissionError:
    print(f"خطأ: لا توجد أذونات للوصول إلى الملف '{filename}'.")
  except Exception as e:
    print(f"حدث خطأ غير متوقع أثناء قراءة الملف '{filename}': {e}")

# حالة: ملف موجود وناجح
read_file_safely('example.txt')

