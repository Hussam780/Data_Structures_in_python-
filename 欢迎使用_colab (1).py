"""### 2.6 الطرق الخاصة (Private Methods)

تمامًا مثل الخصائص، يمكن جعل الطرق (Methods) "خاصة" باستخدام **بادئة الشرطة السفلية المزدوجة (`__`)**.

هذه الطرق مُصممة للاستخدام الداخلي للفئة فقط ولا يمكن استدعاؤها مباشرة من خارجها. تُستخدم عادة للوظائف المساعدة أو التفاصيل التنفيذية التي لا ينبغي أن تكون مكشوفة للعالم الخارجي.
"""

# مثال على طريقة خاصة __validate

class Calculator:
  def __init__(self):
    self.result = 0

  def __validate(self, num): # طريقة خاصة للتحقق من صحة المدخلات
    if not isinstance(num, (int, float)):
      print("المدخل يجب أن يكون رقماً (عدد صحيح أو عشري).")
      return False
    return True

  def add(self, num):
    if self.__validate(num):
      self.result += num
      print(f"تمت إضافة {num}. النتيجة الحالية: {self.result}")
    else:
      print(f"لا يمكن إضافة '{num}'.")

calc = Calculator()
calc.add(10)
calc.add(5.5)
calc.add("نص") # محاولة إضافة قيمة غير صحيحة
print(f"النتيجة النهائية: {calc.result}")

# محاولة استدعاء الدالة الخاصة مباشرة ستؤدي إلى خطأ
try:
  calc.__validate(20)
except AttributeError as e:
  print(f"\nحدث خطأ عند محاولة استدعاء __validate مباشرة: {e}")

